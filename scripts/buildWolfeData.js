const output = [];
const unmatched = [];

for (const [rank, name, category] of WOLFE_LIST) {
  const id = resolve(name);
  if (id === null) {
    unmatched.push({ rank, name, category });
  } else {
    output.push({ id, rank, category });
  }
}

