// Resolve a wolfe name to a pokemon id
function resolve(name) {
  const key = name.toLowerCase().trim();
  if (OVERRIDES[key] !== undefined) return OVERRIDES[key];
  // Direct match
  if (byName[key]) return byName[key].id;
  // Strip " (all forms)" etc
  const stripped = key.replace(/\s*\(.*\)\s*$/, '').trim();
  if (byName[stripped]) return byName[stripped].id;
  return null;
}

